class HistoryItem {
    String nama;
    String imagePath;
    int harga;
    int qty;
    String tanggal;
    String metode;

    HistoryItem(String nama, String imagePath, int harga, int qty, String tanggal, String metode) {
        this.nama = nama;
        this.imagePath = imagePath;
        this.harga = harga;
        this.qty = qty;
        this.tanggal = tanggal;
        this.metode = metode;
    }
}
